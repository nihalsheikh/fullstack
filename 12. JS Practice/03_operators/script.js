console.log("Operators")


/*
Arithmetic Operators: always returns a single number value
+, -, *, /, %
*/
console.log("\n************** Arithmetic Operator *****************")
let a = 5;
let b = 10;

// addition
let result = a + b;
console.log(`Addition is: ${result}`)

// subtraction
result = a - b;
console.log(`Subtraction is: ${result}`)

// multiplication
result = a * b;
console.log(`Multiplication is: ${result}`)

// division
result = a / b;
console.log(`Division is: ${result}`)

// Modulo or remainder of division
let x = 13
result = x % b;
console.log(`Remainder is: ${result}`)

// exponent
result = b ** a
console.log(`Exponential is: ${result}`)

// increment
x++
console.log(`Increment of x is: ${x}`)

// decrement
x--
console.log(`Decrement of x is: ${x}`)
